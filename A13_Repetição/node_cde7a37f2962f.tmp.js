/* 
Repetição 'while' aqui ele primeiro testa a condição e depois executa o bloco 
*/
var c = 1;

while (c <= 6) {
  console.log(`Passo ${c}`);
  c++;
}

/*
Agora iremos ver o 'do while' que iré executar primeiro o bloco e depois testar a condição
*/
var n = 1;
do {
  console.log(`Passo ${n}`);
  c++;
} while (n <= 6);
